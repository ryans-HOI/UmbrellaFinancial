# ============================================================
# Umbrella Financial ??? Windows Server 2022 Bootstrap
# Run as Administrator in PowerShell
# Sets up: IIS, Java 21, PostgreSQL, Docker, Keycloak, AD DS
# ============================================================

Set-ExecutionPolicy Bypass -Scope Process -Force
$ErrorActionPreference = "Stop"

Write-Host "==== Umbrella Financial Bootstrap ====" -ForegroundColor Cyan
Write-Host "DEMO ENVIRONMENT - Insecure by design" -ForegroundColor Yellow

# ?????? 1. Install Chocolatey ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[1/8] Installing Chocolatey..." -ForegroundColor Green
[System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072
Invoke-Expression ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))
refreshenv

# ?????? 2. Install core tools ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[2/8] Installing Java, Node, PostgreSQL, Git..." -ForegroundColor Green
choco install -y openjdk21 nodejs postgresql16 git maven

# Reload PATH
$env:Path = [System.Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [System.Environment]::GetEnvironmentVariable("Path","User")

# ?????? 3. Install IIS + ARR + URL Rewrite ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[3/8] Installing IIS with ARR and URL Rewrite..." -ForegroundColor Green
Enable-WindowsOptionalFeature -Online -FeatureName IIS-WebServerRole, IIS-WebServer, IIS-CommonHttpFeatures, `
    IIS-HttpErrors, IIS-HttpLogging, IIS-RequestFiltering, IIS-StaticContent, `
    IIS-DefaultDocument, IIS-DirectoryBrowsing, IIS-ASPNET45, IIS-NetFxExtensibility45, `
    IIS-ISAPIExtensions, IIS-ISAPIFilter, IIS-WebServerManagementTools, IIS-ManagementConsole `
    -All -NoRestart

# Install URL Rewrite and ARR via Web Platform Installer
$webpiPath = "$env:TEMP\webpi.msi"
Invoke-WebRequest -Uri "https://download.microsoft.com/download/C/F/F/CFF3A0B8-99D4-41A2-AE1A-496C08BEB904/WebPlatformInstaller_amd64_en-US.msi" -OutFile $webpiPath
Start-Process msiexec.exe -Wait -ArgumentList "/i $webpiPath /quiet"

# ARR via direct download
$arrPath = "$env:TEMP\requestRouter.msi"
Invoke-WebRequest -Uri "https://download.microsoft.com/download/E/9/8/E9849D6A-020E-47E4-9FD0-A023E99B54EB/requestRouter_amd64.msi" -OutFile $arrPath
Start-Process msiexec.exe -Wait -ArgumentList "/i $arrPath /quiet"

# URL Rewrite
$urlRewritePath = "$env:TEMP\rewrite_amd64.msi"
Invoke-WebRequest -Uri "https://download.microsoft.com/download/1/2/8/128E2E22-C1B9-44A4-BE2A-5859ED1D4592/rewrite_amd64_en-US.msi" -OutFile $urlRewritePath
Start-Process msiexec.exe -Wait -ArgumentList "/i $urlRewritePath /quiet"

# Enable ARR proxy
Add-WebConfigurationProperty -pspath 'MACHINE/WEBROOT/APPHOST' -filter "system.webServer/proxy" -name "." -value @{enabled="True"}

# ?????? 4. Configure PostgreSQL ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[4/8] Configuring PostgreSQL..." -ForegroundColor Green
$pgBin = "C:\Program Files\PostgreSQL\16\bin"
$env:PGPASSWORD = "postgres"

# Create findb and user
& "$pgBin\psql.exe" -U postgres -c "CREATE DATABASE findb;"
& "$pgBin\psql.exe" -U postgres -c "CREATE USER finapp WITH PASSWORD 'finapp123';"
& "$pgBin\psql.exe" -U postgres -c "GRANT ALL PRIVILEGES ON DATABASE findb TO finapp;"
& "$pgBin\psql.exe" -U postgres -d findb -c "GRANT ALL ON SCHEMA public TO finapp;"

Write-Host "PostgreSQL configured: findb / finapp / finapp123" -ForegroundColor Green

# ?????? 5. Deploy Spring Boot WAR to Tomcat ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[5/8] Setting up Tomcat..." -ForegroundColor Green
choco install -y tomcat

$tomcatHome = "C:\Program Files\Apache Software Foundation\Tomcat 10.1"
$webappsDir = "$tomcatHome\webapps"

# Copy WAR (assumes it's been built and placed in C:\deploy\)
if (Test-Path "C:\deploy\umbrella-financial.war") {
    Copy-Item "C:\deploy\umbrella-financial.war" "$webappsDir\" -Force
    Write-Host "WAR deployed to Tomcat webapps" -ForegroundColor Green
} else {
    Write-Host "WAR not found at C:\deploy\ - deploy manually after build" -ForegroundColor Yellow
}

# Set environment variables for Tomcat service
$tomcatEnv = @"
DB_HOST=localhost
DB_NAME=findb
DB_USER=finapp
DB_PASS=finapp123
LDAP_URL=ldap://localhost:389
LDAP_BASE=DC=umbrella-financial,DC=local
LDAP_USER_DN=CN=svc-finapp,OU=ServiceAccounts,DC=umbrella-financial,DC=local
LDAP_PASSWORD=LdapB1nd!Finance2024
KC_ISSUER=http://localhost:8180/realms/umbrella-financial
KC_CLIENT_ID=finapp-client
KC_CLIENT_SECRET=finapp-secret-2026
APP_URL=https://umbrella-financial.houseofidentity.io
"@

# Write to Tomcat setenv.bat
$setenvContent = $tomcatEnv -split "`n" | ForEach-Object { "set $_" }
Set-Content "$tomcatHome\bin\setenv.bat" ($setenvContent -join "`n")

# ?????? 6. Configure IIS Reverse Proxy ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[6/8] Configuring IIS as reverse proxy..." -ForegroundColor Green

Import-Module WebAdministration

# Remove default site
Remove-WebSite -Name "Default Web Site" -ErrorAction SilentlyContinue

# Create Umbrella Financial site
New-WebSite -Name "UmbrellaFinancial" -Port 80 -PhysicalPath "C:\inetpub\umbrella-financial" -Force
New-Item -ItemType Directory -Force -Path "C:\inetpub\umbrella-financial"

# Web.config for reverse proxy to Tomcat
$webConfig = @'
<?xml version="1.0" encoding="UTF-8"?>
<configuration>
    <system.webServer>
        <!-- INSECURE: Custom server header revealing IIS version -->
        <httpProtocol>
            <customHeaders>
                <add name="X-Powered-By" value="ASP.NET" />
                <add name="Server" value="Microsoft-IIS/10.0" />
                <add name="X-AspNet-Version" value="4.0.30319" />
            </customHeaders>
        </httpProtocol>
        <rewrite>
            <rules>
                <rule name="ReverseProxy" stopProcessing="true">
                    <match url="(.*)" />
                    <action type="Rewrite" url="http://localhost:8080/{R:1}" />
                </rule>
            </rules>
        </rewrite>
        <security>
            <!-- INSECURE: Directory browsing enabled -->
            <requestFiltering allowDoubleEscaping="true" />
        </security>
        <directoryBrowse enabled="true" />
    </system.webServer>
</configuration>
'@
Set-Content "C:\inetpub\umbrella-financial\web.config" $webConfig

# Enable proxy in ARR
Set-WebConfigurationProperty -pspath 'MACHINE/WEBROOT/APPHOST' -filter "system.webServer/proxy" -name "enabled" -value "True"

Write-Host "IIS configured: port 80 ??? Tomcat :8080" -ForegroundColor Green

# ?????? 7. Install Docker Desktop + Keycloak ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[7/8] Installing Docker and Keycloak..." -ForegroundColor Green
choco install -y docker-desktop
Write-Host "Docker Desktop installed. Start it manually, then run:" -ForegroundColor Yellow
Write-Host @"

docker run -d --name keycloak-fin --restart unless-stopped -p 8180:8080 ``
  -e KEYCLOAK_ADMIN=admin -e KEYCLOAK_ADMIN_PASSWORD=admin ``
  -e KC_HTTP_ENABLED=true -e KC_HOSTNAME_STRICT=false ``
  -e KC_HOSTNAME_STRICT_HTTPS=false -e KC_PROXY=edge ``
  quay.io/keycloak/keycloak:24.0.1 start --http-port=8080

"@ -ForegroundColor Cyan

# ?????? 8. Install AD DS (Active Directory Domain Services) ?????????????????????????????????????????????????????????????????????
Write-Host "`n[8/8] Installing AD DS..." -ForegroundColor Green
Install-WindowsFeature -Name AD-Domain-Services -IncludeManagementTools

Write-Host @"

AD DS installed. Now promote to Domain Controller:

Install-ADDSForest ``
  -DomainName "umbrella-financial.local" ``
  -DomainNetbiosName "UMBRELLAFINANCIAL" ``
  -InstallDns:$true ``
  -SafeModeAdministratorPassword (ConvertTo-SecureString "ADSafe!Mode2024" -AsPlainText -Force) ``
  -Force:$true

IMPORTANT: Server will reboot after domain promotion.
After reboot, run: setup-ad-users.ps1

"@ -ForegroundColor Cyan

# ?????? Install Node.js admin console ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`nInstalling admin console..." -ForegroundColor Green
$adminDir = "C:\umbrella-financial\admin-console"
New-Item -ItemType Directory -Force -Path $adminDir

# package.json
@'
{
  "name": "umbrella-financial-admin",
  "version": "1.0.0",
  "main": "server.js",
  "dependencies": {
    "express": "^4.18.2",
    "pg": "^8.11.3"
  }
}
'@ | Set-Content "$adminDir\package.json"

Write-Host "Copy server.js to $adminDir, then: npm install" -ForegroundColor Yellow

# ?????? Create Windows Services ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`nCreating Windows services..." -ForegroundColor Green

# Admin console as Windows service using nssm
choco install -y nssm
nssm install UmbrellaFinancialAdmin node.exe
nssm set UmbrellaFinancialAdmin AppDirectory $adminDir
nssm set UmbrellaFinancialAdmin AppParameters "server.js"
nssm set UmbrellaFinancialAdmin AppEnvironmentExtra "PORT=3011" "DB_HOST=localhost" "DB_NAME=findb" "DB_USER=finapp" "DB_PASS=finapp123" "KC_TOKEN_URL=http://localhost:8180/realms/umbrella-financial/protocol/openid-connect/token" "KC_CLIENT_ID=fin-sim" "KC_CLIENT_SECRET=fin-sim-secret-2026" "SIM_JAVA_BASE_URL=http://localhost:8080/umbrella-financial" "PORTAL_URL=http://localhost:3012"
nssm set UmbrellaFinancialAdmin Start SERVICE_AUTO_START

Write-Host @"

==== Bootstrap Complete ====

Next steps:
1. Promote AD DS to domain controller (run command above, server will reboot)
2. After reboot: run setup-ad-users.ps1 to create AD users
3. Start Docker Desktop, pull Keycloak image, run docker command above
4. Build and deploy umbrella-financial.war to C:\deploy\
5. Configure Keycloak realm (run setup-keycloak.ps1)
6. Run seed-data.sql against findb
7. Copy admin-console/server.js to C:\umbrella-financial\admin-console\
8. npm install in admin console dir
9. Start services: nssm start UmbrellaFinancialAdmin
10. Add DNS A record: umbrella-financial.houseofidentity.io -> this server's public IP

Admin console: http://localhost:3011
Staff portal:  http://localhost:3012
Tomcat/API:    http://localhost:8080/umbrella-financial/health
Keycloak:      http://localhost:8180
"@ -ForegroundColor Cyan
