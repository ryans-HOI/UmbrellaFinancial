$teams     = @("Equity Research","Credit Analysis","Risk Analytics","Quantitative Research","Market Intelligence","Portfolio Analysis")

Write-Host "  Traders..." -ForegroundColor Gray
for ($i = 1; $i -le 80; $i++) {
    $fn  = $traderFn[$i % $traderFn.Count]; $ln = $traderLn[$i % $traderLn.Count]
    $sam = ($fn.Substring(0,1) + $ln + ".trader" + $i).ToLower()
    try {
        New-ADUser -Name "$fn $ln T$i" -GivenName $fn -Surname $ln -SamAccountName $sam `
            -UserPrincipalName "$sam@$domain" -Path "OU=Trading,$domainDN" `
            -AccountPassword (ConvertTo-SecureString "Trade!$i" -AsPlainText -Force) `
            -Enabled $true -PasswordNeverExpires $true -Department ($desks[$i % $desks.Count] + " Desk") -Title "Trader" `
            -Description "INSECURE: PasswordNeverExpires=true, cleartext in DB"
        Add-ADGroupMember -Identity "Traders" -Members $sam
        if ($i -le 10) { Add-ADGroupMember -Identity "SeniorTraders" -Members $sam }
    } catch { }
}

