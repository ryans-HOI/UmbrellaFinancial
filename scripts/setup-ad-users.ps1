try {
    New-ADUser -Name "svc-finapp" -SamAccountName "svc-finapp" `
        -UserPrincipalName "svc-finapp@$domain" `
        -Path "OU=ServiceAccounts,$domainDN" `
        -AccountPassword (ConvertTo-SecureString "LdapB1nd!Finance2024" -AsPlainText -Force) `
        -Enabled $true -PasswordNeverExpires $true -CannotChangePassword $true `
        -Description "INSECURE: LDAP bind account - hardcoded in app config"
    Write-Host "  Created svc-finapp" -ForegroundColor Gray
} catch { Write-Host "  svc-finapp exists" -ForegroundColor DarkGray }

