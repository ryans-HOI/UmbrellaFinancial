Import-Module ActiveDirectory

$domain   = "umbrella-financial.local"
$domainDN = "DC=umbrella-financial,DC=local"

Write-Host "==== Umbrella Financial AD Setup ====" -ForegroundColor Cyan

# ?????? OUs ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[1/5] Creating OUs..." -ForegroundColor Green
@("Trading","Research","Compliance","RiskManagement","RetailBanking","WealthManagement","Executives","IT","ServiceAccounts","Vendors","Disabled") | ForEach-Object {
    try { New-ADOrganizationalUnit -Name $_ -Path $domainDN -ProtectedFromAccidentalDeletion $false; Write-Host "  OU: $_" -ForegroundColor Gray }
    catch { Write-Host "  OU $_ exists" -ForegroundColor DarkGray }
}

# ?????? Groups ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[2/5] Creating groups..." -ForegroundColor Green
@{
    "Traders"="OU=Trading,$domainDN"; "SeniorTraders"="OU=Trading,$domainDN"
    "Analysts"="OU=Research,$domainDN"; "ComplianceOfficers"="OU=Compliance,$domainDN"
    "RiskOfficers"="OU=RiskManagement,$domainDN"; "RetailBankers"="OU=RetailBanking,$domainDN"
    "LoanOfficers"="OU=RetailBanking,$domainDN"; "WealthManagers"="OU=WealthManagement,$domainDN"
    "Executives"="OU=Executives,$domainDN"; "ITAdmins"="OU=IT,$domainDN"
    "ServiceAccounts"="OU=ServiceAccounts,$domainDN"; "Vendors"="OU=Vendors,$domainDN"
}.GetEnumerator() | ForEach-Object {
    try { New-ADGroup -Name $_.Key -GroupScope Global -GroupCategory Security -Path $_.Value; Write-Host "  Group: $($_.Key)" -ForegroundColor Gray }
    catch { Write-Host "  Group $($_.Key) exists" -ForegroundColor DarkGray }
}

