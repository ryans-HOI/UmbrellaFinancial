# ============================================================
# Umbrella Financial ??? Build & Deploy Script (Windows)
# Run from the project root directory
# Requires: Java 21, Maven, Tomcat running
# ============================================================

param(
    [string]$ProjectPath = "C:\umbrella-financial\app",
    [string]$TomcatWebapps = "C:\Program Files\Apache Software Foundation\Tomcat 10.1\webapps",
    [string]$DeployDir = "C:\deploy"
)

Set-ExecutionPolicy Bypass -Scope Process -Force
$ErrorActionPreference = "Stop"

Write-Host "==== Umbrella Financial Build & Deploy ====" -ForegroundColor Cyan

# ?????? Verify tools ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[1] Verifying tools..." -ForegroundColor Green
try {
    $javaVer = & java -version 2>&1
    Write-Host "  Java: OK" -ForegroundColor Gray
} catch {
    Write-Host "ERROR: Java not found. Run bootstrap-windows.ps1 first." -ForegroundColor Red
    exit 1
}

try {
    $mvnVer = & mvn --version 2>&1
    Write-Host "  Maven: OK" -ForegroundColor Gray
} catch {
    Write-Host "ERROR: Maven not found." -ForegroundColor Red
    exit 1
}

# ?????? Create deploy dir ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
New-Item -ItemType Directory -Force -Path $DeployDir | Out-Null
New-Item -ItemType Directory -Force -Path $ProjectPath | Out-Null

# ?????? Copy source if needed ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[2] Checking source..." -ForegroundColor Green
if (-not (Test-Path "$ProjectPath\pom.xml")) {
    Write-Host "  ERROR: pom.xml not found at $ProjectPath" -ForegroundColor Red
    Write-Host "  Copy the umbrella-financial project to $ProjectPath first." -ForegroundColor Yellow
    exit 1
}
Write-Host "  Source found at $ProjectPath" -ForegroundColor Gray

# ?????? Build WAR ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[3] Building WAR with Maven..." -ForegroundColor Green
Push-Location $ProjectPath
try {
    & mvn clean package -DskipTests -q
    if ($LASTEXITCODE -ne 0) { throw "Maven build failed" }
    Write-Host "  Build successful" -ForegroundColor Green
} catch {
    Write-Host "  Build FAILED: $_" -ForegroundColor Red
    Pop-Location
    exit 1
}
Pop-Location

# ?????? Copy WAR ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[4] Copying WAR to deploy dir..." -ForegroundColor Green
$warSource = "$ProjectPath\target\umbrella-financial.war"
if (-not (Test-Path $warSource)) {
    Write-Host "  ERROR: WAR not found at $warSource" -ForegroundColor Red
    exit 1
}
Copy-Item $warSource "$DeployDir\umbrella-financial.war" -Force
Write-Host "  WAR copied to $DeployDir" -ForegroundColor Gray

# ?????? Deploy to Tomcat ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[5] Deploying to Tomcat..." -ForegroundColor Green

# Stop Tomcat service if running
$tomcatService = Get-Service -Name "Tomcat*" -ErrorAction SilentlyContinue
if ($tomcatService) {
    Write-Host "  Stopping Tomcat..." -ForegroundColor Gray
    Stop-Service $tomcatService.Name -Force
    Start-Sleep -Seconds 3
}

# Remove old deployment
$oldWar = "$TomcatWebapps\umbrella-financial.war"
$oldDir = "$TomcatWebapps\umbrella-financial"
if (Test-Path $oldWar) { Remove-Item $oldWar -Force }
if (Test-Path $oldDir) { Remove-Item $oldDir -Recurse -Force }

# Copy new WAR
Copy-Item "$DeployDir\umbrella-financial.war" $TomcatWebapps -Force
Write-Host "  WAR deployed to $TomcatWebapps" -ForegroundColor Green

# ?????? Set Tomcat environment ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[6] Writing Tomcat setenv.bat..." -ForegroundColor Green
$tomcatBin = Split-Path $TomcatWebapps -Parent | Join-Path -ChildPath "bin"
$setenvContent = @"
set DB_HOST=localhost
set DB_PORT=5432
set DB_NAME=findb
set DB_USER=finapp
set DB_PASS=finapp123
set LDAP_URL=ldap://localhost:389
set LDAP_BASE=DC=umbrella-financial,DC=local
set LDAP_USER_DN=CN=svc-finapp,OU=ServiceAccounts,DC=umbrella-financial,DC=local
set LDAP_PASSWORD=LdapB1nd!Finance2024
set KC_ISSUER=http://localhost:8180/realms/umbrella-financial
set KC_CLIENT_ID=finapp-client
set KC_CLIENT_SECRET=finapp-secret-2026
set APP_URL=https://umbrella-financial.houseofidentity.io
"@
Set-Content "$tomcatBin\setenv.bat" $setenvContent
Write-Host "  setenv.bat written" -ForegroundColor Gray

# ?????? Start Tomcat ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[7] Starting Tomcat..." -ForegroundColor Green
if ($tomcatService) {
    Start-Service $tomcatService.Name
    Start-Sleep -Seconds 5
    Write-Host "  Tomcat started" -ForegroundColor Green
} else {
    Write-Host "  Tomcat service not found ??? start manually" -ForegroundColor Yellow
}

# ?????? Health check ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[8] Health check..." -ForegroundColor Green
Start-Sleep -Seconds 10
try {
    $health = Invoke-RestMethod -Uri "http://localhost:8080/umbrella-financial/health" -TimeoutSec 30
    Write-Host "  Status: $($health.status)" -ForegroundColor Green
    Write-Host "  Users: $($health.users)" -ForegroundColor Green
    Write-Host "  Auth flows: $($health.authFlows -join ', ')" -ForegroundColor Green
} catch {
    Write-Host "  Health check failed (app may still be starting): $_" -ForegroundColor Yellow
    Write-Host "  Try: curl http://localhost:8080/umbrella-financial/health" -ForegroundColor Yellow
}

# ?????? Install Node services ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[9] Setting up Node.js services..." -ForegroundColor Green

$adminDir  = "C:\umbrella-financial\admin-console"
$portalDir = "C:\umbrella-financial\staff-portal"

foreach ($dir in @($adminDir, $portalDir)) {
    if (Test-Path "$dir\package.json") {
        Push-Location $dir
        & npm install --quiet
        Pop-Location
        Write-Host "  npm install done: $dir" -ForegroundColor Gray
    } else {
        Write-Host "  SKIP: $dir\package.json not found" -ForegroundColor Yellow
    }
}

# Admin console service
if (Get-Command nssm -ErrorAction SilentlyContinue) {
    $svcName = "UmbrellaFinancialAdmin"
    $existing = Get-Service -Name $svcName -ErrorAction SilentlyContinue
    if (-not $existing) {
        & nssm install $svcName node.exe
        & nssm set $svcName AppDirectory $adminDir
        & nssm set $svcName AppParameters "server.js"
        & nssm set $svcName AppEnvironmentExtra `
            "PORT=3011" "DB_HOST=localhost" "DB_NAME=findb" "DB_USER=finapp" "DB_PASS=finapp123" `
            "KC_TOKEN_URL=http://localhost:8180/realms/umbrella-financial/protocol/openid-connect/token" `
            "KC_CLIENT_ID=fin-sim" "KC_CLIENT_SECRET=fin-sim-secret-2026" `
            "SIM_JAVA_BASE_URL=http://localhost:8080/umbrella-financial" `
            "PORTAL_URL=http://localhost:3012"
        & nssm set $svcName Start SERVICE_AUTO_START
        Write-Host "  $svcName service installed" -ForegroundColor Gray
    }
    Start-Service $svcName -ErrorAction SilentlyContinue
    Write-Host "  $svcName started" -ForegroundColor Green

    # Staff portal service
    $portalSvc = "UmbrellaFinancialPortal"
    $existingPortal = Get-Service -Name $portalSvc -ErrorAction SilentlyContinue
    if (-not $existingPortal) {
        & nssm install $portalSvc node.exe
        & nssm set $portalSvc AppDirectory $portalDir
        & nssm set $portalSvc AppParameters "server.js"
        & nssm set $portalSvc AppEnvironmentExtra `
            "PORT=3012" "DB_HOST=localhost" "DB_NAME=findb" "DB_USER=finapp" "DB_PASS=finapp123" `
            "APP_URL=https://umbrella-financial.houseofidentity.io" `
            "KC_INTERNAL_URL=http://localhost:8180" `
            "KC_PUBLIC_URL=https://umbrella-financial.houseofidentity.io" `
            "KC_REALM=umbrella-financial" `
            "KC_CLIENT_ID=finapp-client" "KC_CLIENT_SECRET=finapp-secret-2026"
        & nssm set $portalSvc Start SERVICE_AUTO_START
        Write-Host "  $portalSvc service installed" -ForegroundColor Gray
    }
    Start-Service $portalSvc -ErrorAction SilentlyContinue
    Write-Host "  $portalSvc started" -ForegroundColor Green
} else {
    Write-Host "  NSSM not found ??? install with: choco install nssm" -ForegroundColor Yellow
}

Write-Host "`n==== Deploy Complete ====" -ForegroundColor Cyan
Write-Host "API Health:     http://localhost:8080/umbrella-financial/health"
Write-Host "Admin Console:  http://localhost:3011  (ryan / Orchid2026!)"
Write-Host "Staff Portal:   http://localhost:3012"
Write-Host "Keycloak:       http://localhost:8180/admin"
Write-Host "Public URL:     https://umbrella-financial.houseofidentity.io"
