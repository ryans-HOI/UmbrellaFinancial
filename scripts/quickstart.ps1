# ============================================================
# Umbrella Financial -- Complete Quickstart
# Run this SINGLE script as Administrator after:
# 1. Windows Server 2022 EC2 is running
# 2. You've RDP'd in
# 3. You've copied the umbrella-financial project to C:\umbrella-financial\
#
# This script orchestrates everything in the correct order.
# ============================================================

Set-ExecutionPolicy Bypass -Scope Process -Force
$ErrorActionPreference = "Continue"
$projectRoot = "C:\umbrella-financial"

function Step($n, $msg) {
    Write-Host "`n[$n] $msg" -ForegroundColor Cyan
}

function OK($msg)   { Write-Host "  [OK] $msg" -ForegroundColor Green }
function WARN($msg) { Write-Host "  [WARN] $msg" -ForegroundColor Yellow }
function ERR($msg)  { Write-Host "  [ERR] $msg" -ForegroundColor Red }

Write-Host "============================================" -ForegroundColor Cyan
Write-Host "  UMBRELLA FINANCIAL -- COMPLETE QUICKSTART " -ForegroundColor Cyan
Write-Host "============================================" -ForegroundColor Cyan

# ?????? Step 1: Chocolatey ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 1 "Installing Chocolatey"
[System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072
Invoke-Expression ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))
refreshenv
OK "Chocolatey installed"

# ?????? Step 2: Core packages ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 2 "Installing Java 21, Node.js, Maven, Git, NSSM, PostgreSQL 16"
choco install -y openjdk21 nodejs maven git nssm postgresql16 --no-progress
refreshenv
$env:Path = [System.Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [System.Environment]::GetEnvironmentVariable("Path","User")
OK "Core packages installed"

# ?????? Step 3: IIS + ARR ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 3 "Installing IIS with ARR and URL Rewrite"
Enable-WindowsOptionalFeature -Online -FeatureName `
    IIS-WebServerRole, IIS-WebServer, IIS-CommonHttpFeatures, IIS-HttpErrors, `
    IIS-HttpLogging, IIS-RequestFiltering, IIS-StaticContent, IIS-DefaultDocument, `
    IIS-DirectoryBrowsing, IIS-ASPNET45, IIS-NetFxExtensibility45, `
    IIS-ISAPIExtensions, IIS-ISAPIFilter, IIS-WebServerManagementTools, IIS-ManagementConsole `
    -All -NoRestart 2>&1 | Out-Null

# URL Rewrite
$urlRewriteUrl = "https://download.microsoft.com/download/1/2/8/128E2E22-C1B9-44A4-BE2A-5859ED1D4592/rewrite_amd64_en-US.msi"
$urlRewritePath = "$env:TEMP\rewrite_amd64.msi"
Invoke-WebRequest -Uri $urlRewriteUrl -OutFile $urlRewritePath -UseBasicParsing
Start-Process msiexec.exe -Wait -ArgumentList "/i $urlRewritePath /quiet"

# ARR
$arrUrl = "https://download.microsoft.com/download/E/9/8/E9849D6A-020E-47E4-9FD0-A023E99B54EB/requestRouter_amd64.msi"
$arrPath = "$env:TEMP\requestRouter_amd64.msi"
Invoke-WebRequest -Uri $arrUrl -OutFile $arrPath -UseBasicParsing
Start-Process msiexec.exe -Wait -ArgumentList "/i $arrPath /quiet"

OK "IIS + ARR installed"

# ?????? Step 4: Tomcat ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 4 "Installing Tomcat 10"
choco install -y tomcat --no-progress
Start-Sleep -Seconds 3
OK "Tomcat installed"

# ?????? Step 5: PostgreSQL setup ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 5 "Configuring PostgreSQL"
$pgBin = "C:\Program Files\PostgreSQL\16\bin"
$env:PGPASSWORD = "postgres"
Start-Service postgresql-x64-16 -ErrorAction SilentlyContinue
Start-Sleep -Seconds 3

& "$pgBin\psql.exe" -U postgres -c "CREATE DATABASE findb;" 2>&1 | Out-Null
& "$pgBin\psql.exe" -U postgres -c "CREATE USER finapp WITH PASSWORD 'finapp123';" 2>&1 | Out-Null
& "$pgBin\psql.exe" -U postgres -c "GRANT ALL PRIVILEGES ON DATABASE findb TO finapp;" 2>&1 | Out-Null
& "$pgBin\psql.exe" -U postgres -d findb -c "GRANT ALL ON SCHEMA public TO finapp;" 2>&1 | Out-Null
OK "findb created, finapp user created"

# ?????? Step 6: Seed database ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 6 "Seeding database"
$seedFile = "$projectRoot\scripts\seed-data.sql"
if (Test-Path $seedFile) {
    $env:PGPASSWORD = "finapp123"
    & "$pgBin\psql.exe" -U finapp -d findb -f $seedFile 2>&1 | Out-Null
    OK "Database seeded"
} else {
    WARN "seed-data.sql not found at $seedFile -- seed manually later"
}

# ?????? Step 7: Copy project files to service dirs ????????????????????????????????????????????????????????????????????????????????????????????????
Step 7 "Setting up service directories"

$adminDir  = "C:\umbrella-financial\admin-console"
$portalDir = "C:\umbrella-financial\staff-portal"
New-Item -ItemType Directory -Force -Path $adminDir  | Out-Null
New-Item -ItemType Directory -Force -Path $portalDir | Out-Null
New-Item -ItemType Directory -Force -Path "C:\deploy" | Out-Null

# Copy Node files
if (Test-Path "$projectRoot\admin-console\server.js") {
    Copy-Item "$projectRoot\admin-console\*" $adminDir -Force
    OK "Admin console files copied"
} else { WARN "Admin console server.js not found in project" }

if (Test-Path "$projectRoot\staff-portal\server.js") {
    Copy-Item "$projectRoot\staff-portal\*" $portalDir -Force
    OK "Staff portal files copied"
} else { WARN "Staff portal server.js not found in project" }

# npm install
Push-Location $adminDir
& npm install --quiet 2>&1 | Out-Null
Pop-Location
OK "Admin console npm install done"

Push-Location $portalDir
& npm install --quiet 2>&1 | Out-Null
Pop-Location
OK "Staff portal npm install done"

# ?????? Step 8: Build WAR ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 8 "Building Spring Boot WAR"
if (Test-Path "$projectRoot\pom.xml") {
    Push-Location $projectRoot
    & mvn clean package -DskipTests -q 2>&1 | Out-Null
    if ($LASTEXITCODE -eq 0) {
        $tomcatWebapps = "C:\Program Files\Apache Software Foundation\Tomcat 10.1\webapps"
        if (-not (Test-Path $tomcatWebapps)) {
            # Try alternate path
            $tomcatWebapps = (Get-ChildItem "C:\Program Files\Apache Software Foundation" -Filter "Tomcat*" | Select-Object -First 1).FullName + "\webapps"
        }
        Copy-Item "$projectRoot\target\umbrella-financial.war" $tomcatWebapps -Force
        OK "WAR built and deployed to Tomcat"

        # Write setenv.bat
        $tomcatBin = Split-Path $tomcatWebapps -Parent | Join-Path -ChildPath "bin"
        @"
set DB_HOST=localhost
set DB_PORT=5432
set DB_NAME=findb
set DB_USER=finapp
set DB_PASS=finapp123
set LDAP_URL=ldap://localhost:389
set LDAP_BASE=DC=umbrella-financial,DC=local
set LDAP_USER_DN=CN=svc-finapp,OU=ServiceAccounts,DC=umbrella-financial,DC=local
set LDAP_PASSWORD=LdapB1nd!Finance2024
set KC_ISSUER=http://localhost:8180/realms/umbrella-financial
set KC_CLIENT_ID=finapp-client
set KC_CLIENT_SECRET=finapp-secret-2026
set APP_URL=https://umbrella-financial.houseofidentity.io
"@ | Set-Content "$tomcatBin\setenv.bat"
        OK "Tomcat setenv.bat written"
    } else {
        ERR "Maven build failed -- check Java/Maven installation"
    }
    Pop-Location
} else {
    WARN "pom.xml not found -- build WAR manually with: mvn clean package -DskipTests"
}

# ?????? Step 9: Configure IIS ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 9 "Configuring IIS reverse proxy"
& "$projectRoot\scripts\setup-iis-ssl.ps1"
OK "IIS configured"

# ?????? Step 10: Install Windows services ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 10 "Installing Windows services (NSSM)"

# Admin console
& nssm install UmbrellaFinancialAdmin node.exe 2>&1 | Out-Null
& nssm set UmbrellaFinancialAdmin AppDirectory $adminDir 2>&1 | Out-Null
& nssm set UmbrellaFinancialAdmin AppParameters "server.js" 2>&1 | Out-Null
& nssm set UmbrellaFinancialAdmin AppEnvironmentExtra "PORT=3011" "DB_HOST=localhost" "DB_NAME=findb" "DB_USER=finapp" "DB_PASS=finapp123" "KC_TOKEN_URL=http://localhost:8180/realms/umbrella-financial/protocol/openid-connect/token" "KC_CLIENT_ID=fin-sim" "KC_CLIENT_SECRET=fin-sim-secret-2026" "SIM_JAVA_BASE_URL=http://localhost:8080/umbrella-financial" "PORTAL_URL=http://localhost:3012" 2>&1 | Out-Null
& nssm set UmbrellaFinancialAdmin Start SERVICE_AUTO_START 2>&1 | Out-Null
Start-Service UmbrellaFinancialAdmin -ErrorAction SilentlyContinue
OK "Admin console service installed and started"

# Staff portal
& nssm install UmbrellaFinancialPortal node.exe 2>&1 | Out-Null
& nssm set UmbrellaFinancialPortal AppDirectory $portalDir 2>&1 | Out-Null
& nssm set UmbrellaFinancialPortal AppParameters "server.js" 2>&1 | Out-Null
& nssm set UmbrellaFinancialPortal AppEnvironmentExtra "PORT=3012" "DB_HOST=localhost" "DB_NAME=findb" "DB_USER=finapp" "DB_PASS=finapp123" "APP_URL=https://umbrella-financial.houseofidentity.io" "KC_INTERNAL_URL=http://localhost:8180" "KC_PUBLIC_URL=https://umbrella-financial.houseofidentity.io" "KC_REALM=umbrella-financial" "KC_CLIENT_ID=finapp-client" "KC_CLIENT_SECRET=finapp-secret-2026" 2>&1 | Out-Null
& nssm set UmbrellaFinancialPortal Start SERVICE_AUTO_START 2>&1 | Out-Null
Start-Service UmbrellaFinancialPortal -ErrorAction SilentlyContinue
OK "Staff portal service installed and started"

# ?????? Step 11: Docker + Keycloak ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 11 "Docker + Keycloak"
WARN "Docker Desktop requires manual installation and restart."
Write-Host @"

  After installing Docker Desktop and restarting, run:

  docker run -d --name keycloak-fin --restart unless-stopped -p 8180:8080 ``
    -e KEYCLOAK_ADMIN=admin -e KEYCLOAK_ADMIN_PASSWORD=admin ``
    -e KC_HTTP_ENABLED=true -e KC_HOSTNAME_STRICT=false ``
    -e KC_HOSTNAME_STRICT_HTTPS=false -e KC_PROXY=edge ``
    quay.io/keycloak/keycloak:24.0.1 start --http-port=8080

  Then run: bash scripts/setup-keycloak.sh (from WSL or Git Bash)

"@ -ForegroundColor Yellow

choco install -y docker-desktop --no-progress 2>&1 | Out-Null
OK "Docker Desktop installer launched"

# ?????? Step 12: AD DS ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step 12 "Active Directory Domain Services"
Install-WindowsFeature -Name AD-Domain-Services -IncludeManagementTools 2>&1 | Out-Null
OK "AD DS feature installed"

Write-Host @"

  Promote to Domain Controller (server will REBOOT):

  Install-ADDSForest ``
    -DomainName "umbrella-financial.local" ``
    -DomainNetbiosName "UMBRELLAFINANCIAL" ``
    -InstallDns:`$true ``
    -SafeModeAdministratorPassword (ConvertTo-SecureString "ADSafe!Mode2024" -AsPlainText -Force) ``
    -Force:`$true

  After reboot, run: .\scripts\setup-ad-users.ps1

"@ -ForegroundColor Yellow

# ?????? Final summary ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n============================================" -ForegroundColor Cyan
Write-Host "  QUICKSTART COMPLETE" -ForegroundColor Cyan
Write-Host "============================================" -ForegroundColor Cyan
Write-Host ""
Write-Host "Services status:"
foreach ($svc in @("W3SVC","postgresql-x64-16","UmbrellaFinancialAdmin","UmbrellaFinancialPortal")) {
    $s = Get-Service -Name $svc -ErrorAction SilentlyContinue
    if ($s) {
        $color = if ($s.Status -eq "Running") { "Green" } else { "Red" }
        Write-Host "  $($s.Name): $($s.Status)" -ForegroundColor $color
    }
}
Write-Host ""
Write-Host "Endpoints (after Tomcat starts ~30s):"
Write-Host "  API Health:    http://localhost:8080/umbrella-financial/health"
Write-Host "  Admin Console: http://localhost:3011  (ryan / Orchid2026!)"
Write-Host "  Staff Portal:  http://localhost:3012"
Write-Host "  Keycloak:      http://localhost:8180/admin (after Docker setup)"
Write-Host ""
Write-Host "Still needed:"
Write-Host "  1. Promote AD DS and run setup-ad-users.ps1 (after reboot)"
Write-Host "  2. Start Docker Desktop and pull Keycloak image"
Write-Host "  3. Run setup-keycloak.sh"
Write-Host "  4. Add DNS A record: umbrella-financial.houseofidentity.io -> this IP"
Write-Host "  5. SSL cert via win-acme: wacs --target iis --host umbrella-financial.houseofidentity.io"
