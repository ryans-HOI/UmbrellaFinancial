# ============================================================
# Umbrella Financial ??? IIS SSL & Site Configuration
# Run after bootstrap and domain cert is available
# Configures: HTTPS, ARR reverse proxy, custom headers
# ============================================================

param(
    [string]$Domain = "umbrella-financial.houseofidentity.io",
    [string]$CertThumbprint = "",  # Set after importing cert
    [string]$BackendUrl = "http://localhost:8080"
)

Import-Module WebAdministration
Set-ExecutionPolicy Bypass -Scope Process -Force

Write-Host "==== IIS SSL & Site Configuration ====" -ForegroundColor Cyan

# ?????? Enable ARR proxy globally ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[1] Enabling ARR proxy..." -ForegroundColor Green
try {
    Add-WebConfigurationProperty -pspath 'MACHINE/WEBROOT/APPHOST' `
        -filter "system.webServer/proxy" -name "." -value @{enabled="True"}
    Set-WebConfigurationProperty -pspath 'MACHINE/WEBROOT/APPHOST' `
        -filter "system.webServer/proxy" -name "enabled" -value "True"
    Write-Host "  ARR proxy enabled" -ForegroundColor Gray
} catch {
    Write-Host "  ARR may already be enabled: $_" -ForegroundColor Yellow
}

# ?????? Create site directory ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[2] Creating site directory..." -ForegroundColor Green
$sitePath = "C:\inetpub\umbrella-financial"
New-Item -ItemType Directory -Force -Path $sitePath | Out-Null

# ?????? Write web.config ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[3] Writing web.config..." -ForegroundColor Green
$webConfig = @"
<?xml version="1.0" encoding="UTF-8"?>
<configuration>
    <system.webServer>
        <!-- INSECURE: reveals exact IIS and ASP.NET versions -->
        <httpProtocol>
            <customHeaders>
                <add name="X-Powered-By" value="ASP.NET" />
                <add name="X-AspNet-Version" value="4.0.30319" />
                <add name="X-Frame-Options" value="ALLOW" />
            </customHeaders>
        </httpProtocol>

        <!-- Reverse proxy to Tomcat -->
        <rewrite>
            <rules>
                <!-- Admin console -->
                <rule name="AdminConsole" stopProcessing="true">
                    <match url="^admin/(.*)" />
                    <action type="Rewrite" url="http://localhost:3011/{R:1}" />
                </rule>
                <!-- Staff portal -->
                <rule name="StaffPortal" stopProcessing="true">
                    <match url="^portal/(.*)" />
                    <action type="Rewrite" url="http://localhost:3012/{R:1}" />
                </rule>
                <!-- Keycloak auth paths -->
                <rule name="Keycloak" stopProcessing="true">
                    <match url="^(realms|resources|js|auth)/(.*)" />
                    <action type="Rewrite" url="http://localhost:8180/{R:0}" />
                </rule>
                <!-- Everything else to Tomcat -->
                <rule name="TomcatProxy" stopProcessing="true">
                    <match url="(.*)" />
                    <action type="Rewrite" url="${BackendUrl}/{R:1}" />
                </rule>
            </rules>
            <!-- HTTPS redirect -->
            <outboundRules>
                <rule name="HTTPS redirect" preCondition="ResponseIsHtml">
                    <match filterByTags="A, Form, Img, Input, Link, Script" pattern="^http://([^/]+)/(.*)" />
                    <action type="Rewrite" value="https://{R:1}/{R:2}" />
                </rule>
            </outboundRules>
        </rewrite>

        <!-- INSECURE: directory browsing enabled -->
        <directoryBrowse enabled="true" />

        <!-- INSECURE: detailed errors exposed -->
        <httpErrors errorMode="Detailed" />

        <security>
            <requestFiltering>
                <!-- INSECURE: allows double escaping -->
                <requestLimits maxAllowedContentLength="104857600" />
            </requestFiltering>
        </security>
    </system.webServer>
</configuration>
"@
Set-Content "$sitePath\web.config" $webConfig -Encoding UTF8
Write-Host "  web.config written" -ForegroundColor Gray

# ?????? Remove default site ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[4] Removing default IIS site..." -ForegroundColor Green
Remove-WebSite -Name "Default Web Site" -ErrorAction SilentlyContinue
Write-Host "  Default site removed" -ForegroundColor Gray

# ?????? Create HTTP site ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[5] Creating IIS site..." -ForegroundColor Green
$siteName = "UmbrellaFinancial"

Remove-WebSite -Name $siteName -ErrorAction SilentlyContinue

New-WebSite -Name $siteName -Port 80 -PhysicalPath $sitePath -HostHeader $Domain -Force
Write-Host "  HTTP site created: $Domain:80" -ForegroundColor Gray

# ?????? SSL binding (if cert thumbprint provided) ???????????????????????????????????????????????????????????????????????????????????????????????????
if ($CertThumbprint -ne "") {
    Write-Host "`n[6] Adding HTTPS binding..." -ForegroundColor Green
    New-WebBinding -Name $siteName -Protocol https -Port 443 -HostHeader $Domain -SslFlags 1
    $binding = Get-WebBinding -Name $siteName -Protocol https
    $binding.AddSslCertificate($CertThumbprint, "My")
    Write-Host "  HTTPS binding added with cert: $CertThumbprint" -ForegroundColor Green
} else {
    Write-Host "`n[6] Skipping SSL (no cert thumbprint provided)" -ForegroundColor Yellow
    Write-Host "  To add SSL later:" -ForegroundColor Yellow
    Write-Host "  1. Import cert to Windows cert store" -ForegroundColor Yellow
    Write-Host "  2. Get thumbprint: Get-ChildItem Cert:\LocalMachine\My" -ForegroundColor Yellow
    Write-Host "  3. Re-run: .\setup-iis-ssl.ps1 -CertThumbprint <thumbprint>" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "  For Let's Encrypt on Windows, use win-acme:" -ForegroundColor Yellow
    Write-Host "  choco install win-acme" -ForegroundColor Yellow
    Write-Host "  wacs --target iis --host $Domain --installation iis" -ForegroundColor Yellow
}

# ?????? Application Pool ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[7] Configuring Application Pool..." -ForegroundColor Green
$appPool = "UmbrellaFinancialPool"
if (-not (Test-Path "IIS:\AppPools\$appPool")) {
    New-WebAppPool -Name $appPool
}
Set-ItemProperty "IIS:\AppPools\$appPool" -Name processModel.identityType -Value NetworkService
Set-ItemProperty "IIS:\AppPools\$appPool" -Name managedRuntimeVersion -Value ""
Set-WebConfigurationProperty -Filter "/system.applicationHost/sites/site[@name='$siteName']" `
    -Name "applicationDefaults.applicationPool" -Value $appPool
Write-Host "  App pool configured" -ForegroundColor Gray

# ?????? Firewall rules ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[8] Adding firewall rules..." -ForegroundColor Green
$rules = @(
    @{Name="HTTP-80";   Port=80},
    @{Name="HTTPS-443"; Port=443},
    @{Name="Tomcat-8080"; Port=8080},
    @{Name="Keycloak-8180"; Port=8180},
    @{Name="AdminConsole-3011"; Port=3011},
    @{Name="StaffPortal-3012"; Port=3012},
    @{Name="LDAP-389"; Port=389},
    @{Name="LDAPS-636"; Port=636}
)

foreach ($rule in $rules) {
    New-NetFirewallRule -DisplayName "UmbrellaFin-$($rule.Name)" `
        -Direction Inbound -Protocol TCP -LocalPort $rule.Port `
        -Action Allow -ErrorAction SilentlyContinue | Out-Null
    Write-Host "  Firewall: port $($rule.Port) open" -ForegroundColor Gray
}

# ?????? Verify IIS is running ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n[9] Starting IIS..." -ForegroundColor Green
Start-Service W3SVC -ErrorAction SilentlyContinue
$w3svc = Get-Service W3SVC
Write-Host "  IIS Status: $($w3svc.Status)" -ForegroundColor $(if($w3svc.Status -eq 'Running'){'Green'} else {'Red'})

# ?????? Summary ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Write-Host "`n==== IIS Configuration Complete ====" -ForegroundColor Cyan
Write-Host "Site:         $Domain"
Write-Host "HTTP:         http://$Domain"
if ($CertThumbprint) { Write-Host "HTTPS:        https://$Domain" }
Write-Host "Local test:   http://localhost/umbrella-financial/health"
Write-Host ""
Write-Host "Routing:"
Write-Host "  /           ??? Tomcat :8080 (Spring Boot API)"
Write-Host "  /admin/     ??? Admin Console :3011"
Write-Host "  /portal/    ??? Staff Portal :3012"
Write-Host "  /realms/    ??? Keycloak :8180"
